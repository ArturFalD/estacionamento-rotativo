    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Motorista m = new Motorista();
        MotoristaDAO dao = new MotoristaDAO();
        m.setIdMotorista(Integer.parseInt(LIdMotorista.getText()));
        m.setNome(jTFNome.getText());
        if (Masc.isSelected()){
            m.setGenero("Masculino");
        } else if (Fem.isSelected()){
            m.setGenero("Feminino");
        } else {
            m.setGenero("Outro");
        }
        m.setRg(jTFRG.getText());
        m.setCpf(jTFCPF.getText());
        m.setCelular(jTFCelular.getText());
        m.setEmail(jTFEmail.getText());
        m.setSenha(jTFSenha.getText());
        
        dao.update(m);
        dispose();
    }//GEN-LAST:event_jButton1ActionPerformed

